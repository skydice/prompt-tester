"""
피팅 설명 생성 프롬프트 v1
입력: 의류 실측 데이터 + 구매 고객 후기
출력: 신규 상품에 대한 예상 피팅 설명
"""

SYSTEM_PROMPT = """당신은 패션 브랜드의 피팅 전문 어드바이저입니다.
의류의 실측 데이터와 실제 구매 고객들의 착용 후기를 분석하여,
새로운 고객이 구매 전에 피팅감을 정확하게 예상할 수 있도록 설명합니다.

## 역할
- 수치 데이터를 사람이 이해할 수 있는 언어로 변환
- 고객 후기에서 반복 패턴을 추출하여 신뢰도 높은 피팅 정보 제공
- 체형별로 어떻게 다르게 맞는지 구체적으로 설명

## 출력 원칙
- 수치 나열이 아닌, 실제 착용 경험을 상상할 수 있는 언어 사용
- 긍정/부정 모두 솔직하게 기술 (신뢰도 확보)
- 애매한 표현 금지: "약간 크다" → "어깨 너비가 1-2cm 여유 있어 레이어링에 유리"
- 체형/신체 부위별로 구분하여 설명"""


USER_PROMPT_TEMPLATE = """다음 데이터를 분석하여 이 상품의 피팅 설명을 작성해주세요.

## 상품 실측 데이터
{measurements}

## 브랜드 사이즈 가이드 (참고용)
{size_guide}

## 실제 구매 고객 후기 ({review_count}개)
{reviews}

---

## 요청 출력 형식

### 1. 핵심 피팅 요약 (2-3줄)
전반적인 핏의 특성을 간결하게. 처음 보는 고객이 5초 안에 핵심을 파악할 수 있도록.

### 2. 부위별 착용감
다음 부위 중 해당하는 항목만 작성 (상의: 어깨/가슴/소매/기장, 하의: 허리/힙/허벅지/밑위/기장):
- 실측 수치와 일반적인 체형 기준을 연결하여 설명
- 고객 후기에서 해당 부위 언급이 많으면 그 내용 반영

### 3. 사이즈 선택 가이드
- 평소 사이즈대로 선택하면 되는지 여부
- 특정 체형(마른 체형 / 근육형 / 허리 대비 힙이 큰 경우 등)에 대한 조언
- 예: "평소 M 착용 시 → M 권장 (단, 어깨가 넓다면 L 고려)"

### 4. 이런 분께 잘 맞아요 / 주의하세요
- 잘 맞는 체형/스타일 선호
- 주의가 필요한 체형/선호 (솔직하게)

### 5. 후기 기반 신뢰도
- 분석에 사용된 후기 수
- 후기 일치도: 높음/중간/낮음 (후기 내용이 일관적일수록 높음)
- 주요 언급 키워드 (예: #어깨넓음 #기장짧음 #허리여유)"""


def build_prompt(
    measurements: dict,
    size_guide: dict,
    reviews: list[str],
) -> tuple[str, str]:
    """
    Returns (system_prompt, user_prompt)

    measurements: {"총장": "65cm", "가슴": "100cm", ...}
    size_guide: {"S": {"가슴": "88-92"}, "M": {"가슴": "92-96"}, ...}
    reviews: ["리뷰 텍스트1", "리뷰 텍스트2", ...]
    """
    measurements_text = "\n".join(
        f"- {k}: {v}" for k, v in measurements.items()
    )

    size_guide_text = ""
    for size, dims in size_guide.items():
        dims_str = ", ".join(f"{k} {v}" for k, v in dims.items())
        size_guide_text += f"- {size}: {dims_str}\n"

    reviews_text = "\n".join(
        f"[{i+1}] {r}" for i, r in enumerate(reviews)
    )

    user_prompt = USER_PROMPT_TEMPLATE.format(
        measurements=measurements_text,
        size_guide=size_guide_text.strip(),
        reviews=reviews_text,
        review_count=len(reviews),
    )

    return SYSTEM_PROMPT, user_prompt


